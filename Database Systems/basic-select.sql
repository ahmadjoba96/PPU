-- Examples of basic SELECT queries practiced during the course.

SELECT * 
FROM EMP;


SELECT EMPNO, ENAME, JOB, SAL 
FROM EMP;


SELECT * FROM 
EMP WHERE JOB = 'MANAGER';


SELECT ENAME, SAL 
FROM EMP WHERE SAL > 2000;


SELECT ENAME, SAL 
FROM EMP 
ORDER BY SAL DESC;


SELECT ENAME, DEPTNO 
FROM EMP 
WHERE DEPTNO = 1;


SELECT ENAME, COMM 
FROM EMP 
WHERE COMM IS NOT NULL;


SELECT ENAME, MGR 
FROM EMP;


SELECT DISTINCT JOB 
FROM EMP;


SELECT DEPTNO, COUNT(*) AS NUM_EMP 
FROM EMP GROUP BY DEPTNO;


SELECT JOB, AVG(SAL) AS AVG_SALARY 
FROM EMP GROUP BY JOB;


SELECT * 
FROM DEPT;


SELECT DNAME, LOC 
FROM DEPT;


SELECT EMP.ENAME, DEPT.DNAME
FROM EMP JOIN DEPT ON EMP.DEPTNO = DEPT.DEPTNO;


SELECT ENAME, HIREDATE 
FROM EMP 
WHERE HIREDATE > TO_DATE('01-01-2020', 'DD-MM-YYYY');


